import { UUID } from "crypto";
import { Table, Column, Model } from "sequelize-typescript";

@Table({ freezeTableName: true })
export class PostComment extends Model {
	// Columns
	@Column({ primaryKey: true })
	id: number;

	@Column
	user_id: UUID;

	@Column
	post_id: UUID;

	@Column
	content: string;

	// Relationship
}
